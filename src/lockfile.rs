impl PlatformInfo {
    /// Returns true if this PlatformInfo has no meaningful data (for serde skip)
    pub fn is_empty(&self) -> bool {
        self.install.is_none()
            && self.checksum.is_none()
            && self.url.is_none()
            && self.url_api.is_none()
            && self.conda_deps.is_none()
            && self.pkgx_deps.is_none()
            && self.pkgx_provides.is_none()
            && self.pkgx_runtime_env.is_none()
            && self.provenance.is_none()
    }

    /// True when the lockfile has checksum-backed, successfully verified provenance.
    pub fn has_checksum_and_verified_provenance(&self) -> bool {
        self.checksum.is_some() && self.provenance.is_some()
    }

    /// Merge this PlatformInfo with another, preserving important data.
    /// - Prefers sha256 checksums over blake3 (more portable/verifiable)
    /// - Preserves URL if missing in self
    /// - Preserves url_api if missing in self
    /// - Drops the other side's checksum/size/url_api when URLs disagree, since
    ///   those fields describe a specific artifact and become stale if the URL
    ///   changes.
    pub fn merge_with(&self, other: &PlatformInfo) -> PlatformInfo {
        let url_changed = self.url.is_some() && other.url.is_some() && self.url != other.url;
        let install_changed = self.install.is_some() && self.install != other.install;
        let artifact_changed = url_changed
            || install_changed
            || (self.url.is_some() && other.install.as_deref() == Some("source"));

        // For checksums, prefer sha256 over blake3 since sha256 comes from
        // official releases and is more portable/verifiable. If artifacts disagree,
        // ignore the other side's artifact-bound fields entirely.
        let checksum = if artifact_changed {
            self.checksum.clone()
        } else {
            match (&self.checksum, &other.checksum) {
                (Some(self_cs), Some(other_cs)) => {
                    let self_is_sha256 = self_cs.starts_with("sha256:");
                    let other_is_sha256 = other_cs.starts_with("sha256:");
                    match (self_is_sha256, other_is_sha256) {
                        (true, _) => Some(self_cs.clone()),
                        (false, true) => Some(other_cs.clone()),
                        (false, false) => Some(self_cs.clone()), // both blake3, use self
                    }
                }
                (Some(cs), None) | (None, Some(cs)) => Some(cs.clone()),
                (None, None) => None,
            }
        };

        let size = if artifact_changed {
            self.size
        } else {
            self.size.or(other.size)
        };

        let url_api = if artifact_changed {
            self.url_api.clone()
        } else {
            self.url_api.clone().or_else(|| other.url_api.clone())
        };

        let provenance = if artifact_changed {
            self.provenance.clone()
        } else {
            match (self.provenance.clone(), other.provenance.clone()) {
                (Some(a), Some(b)) => Some(a.merge(b)),
                (a, b) => a.or(b),
            }
        };
        PlatformInfo {
            install: self.install.clone().or_else(|| {
                if self.url.is_some() {
                    None
                } else {
                    other.install.clone()
                }
            }),
            checksum,
            size,
            url: if artifact_changed {
                self.url.clone()
            } else {
                self.url.clone().or_else(|| other.url.clone())
            },
            url_api,
            conda_deps: self.conda_deps.clone().or_else(|| other.conda_deps.clone()),
            pkgx_deps: self.pkgx_deps.clone().or_else(|| other.pkgx_deps.clone()),
            pkgx_provides: self
                .pkgx_provides
                .clone()
                .or_else(|| other.pkgx_provides.clone()),
            pkgx_runtime_env: self
                .pkgx_runtime_env
                .clone()
                .or_else(|| other.pkgx_runtime_env.clone()),
            provenance,
            github_attestations: None,
        }
    }
}

impl TryFrom<toml::Value> for PlatformInfo {
    type Error = Report;
    fn try_from(value: toml::Value) -> Result<Self> {
        match value {
            toml::Value::String(checksum) => Ok(PlatformInfo {
                checksum: Some(checksum),
                ..Default::default()
            }),
            toml::Value::Table(mut t) => {
                let install = match t.remove("install") {
                    Some(toml::Value::String(s)) if s == "source" => Some(s),
                    Some(toml::Value::String(s)) => {
                        bail!("unrecognized install {s:?} in lockfile")
                    }
                    _ => None,
                };
                let checksum = match t.remove("checksum") {
                    Some(toml::Value::String(s)) => Some(s),
                    _ => None,
                };
                let size = t
                    .remove("size")
                    .and_then(|v| v.as_integer())
                    .map(|i| i.try_into())
                    .transpose()?;
                let url = match t.remove("url") {
                    Some(toml::Value::String(s)) => Some(s),
                    _ => None,
                };
                let url_api = match t.remove("url_api") {
                    Some(toml::Value::String(s)) => Some(s),
                    _ => None,
                };
                let conda_deps = match t.remove("conda_deps") {
                    Some(toml::Value::Array(arr)) => Some(
                        arr.into_iter()
                            .filter_map(|v| v.as_str().map(String::from))
                            .collect(),
                    ),
                    _ => None,
                };
                let github_attestations = match t.remove("github_attestations") {
                    Some(toml::Value::String(s)) if s == "unavailable" => {
                        Some(GithubAttestationsStatus::Unavailable)
                    }
                    Some(toml::Value::String(s)) => {
                        bail!("unrecognized github_attestations status {s:?} in lockfile")
                    }
                    _ => None,
                };
                let pkgx_deps = match t.remove("pkgx_deps") {
                    Some(toml::Value::Array(arr)) => Some(
                        arr.into_iter()
                            .filter_map(|v| v.as_str().map(String::from))
                            .collect(),
                    ),
                    _ => None,
                };
                let pkgx_provides = match t.remove("pkgx_provides") {
                    Some(toml::Value::Array(arr)) => Some(
                        arr.into_iter()
                            .filter_map(|v| v.as_str().map(String::from))
                            .collect(),
                    ),
                    _ => None,
                };
                let pkgx_runtime_env = match t.remove("pkgx_runtime_env") {
                    Some(toml::Value::Table(table)) => {
                        Some(string_table_from_toml_value(toml::Value::Table(table))?)
                    }
                    _ => None,
                };
                // Legacy: read provenance_url for backwards compat with old lockfiles
                let legacy_provenance_url = match t.remove("provenance_url") {
                    Some(toml::Value::String(s)) => Some(s),
                    _ => None,
                };
                let provenance = match t.remove("provenance") {
                    Some(toml::Value::String(s)) => {
                        let mut prov: ProvenanceType = s
                            .parse()
                            .map_err(|_| eyre!("unrecognized provenance type {s:?} in lockfile"))?;
                        // Attach legacy provenance_url to SLSA if present
                        if let ProvenanceType::Slsa { ref mut url } = prov {
                            *url = legacy_provenance_url;
                        }
                        Some(prov)
                    }
                    Some(toml::Value::Table(mut prov_table)) => {
                        if let Some(slsa_val) = prov_table.remove("slsa") {
                            let slsa_url = match slsa_val {
                                toml::Value::Table(mut st) => match st.remove("url") {
                                    Some(toml::Value::String(u)) => Some(u),
                                    _ => None,
                                },
                                _ => None,
                            };
                            Some(ProvenanceType::Slsa { url: slsa_url })
                        } else {
                            // Unknown table variant
                            let keys: Vec<_> = prov_table.keys().cloned().collect();
                            bail!(
                                "unrecognized provenance table format in lockfile: {:?}",
                                keys
                            );
                        }
                    }
                    _ => None,
                };
                let github_attestations = if provenance.is_some() {
                    None
                } else {
                    github_attestations
                };
                Ok(PlatformInfo {
                    install,
                    checksum,
                    size,
                    url,
                    url_api,
                    conda_deps,
                    pkgx_deps,
                    pkgx_provides,
                    pkgx_runtime_env,
                    provenance,
                    github_attestations,
                })
            }
            _ => bail!("unsupported asset info format"),
        }
    }
}

impl From<PlatformInfo> for toml::Value {
    fn from(platform_info: PlatformInfo) -> Self {
        let mut table = toml::Table::new();
        if let Some(install) = platform_info.install {
            table.insert("install".to_string(), install.into());
        }
        if let Some(checksum) = platform_info.checksum {
            table.insert("checksum".to_string(), checksum.into());
        }
        if let Some(url) = platform_info.url {
            table.insert("url".to_string(), url.into());
        }
        if let Some(url_api) = platform_info.url_api {
            table.insert("url_api".to_string(), url_api.into());
        }
        if let Some(conda_deps) = platform_info.conda_deps {
            let deps: toml::Value = conda_deps
                .into_iter()
                .map(toml::Value::String)
                .collect::<Vec<_>>()
                .into();
            table.insert("conda_deps".to_string(), deps);
        }
        if let Some(pkgx_deps) = platform_info.pkgx_deps {
            let deps: toml::Value = pkgx_deps
                .into_iter()
                .map(toml::Value::String)
                .collect::<Vec<_>>()
                .into();
            table.insert("pkgx_deps".to_string(), deps);
        }
        if let Some(pkgx_provides) = platform_info.pkgx_provides {
            let provides: toml::Value = pkgx_provides
                .into_iter()
                .map(toml::Value::String)
                .collect::<Vec<_>>()
                .into();
            table.insert("pkgx_provides".to_string(), provides);
        }
        if let Some(pkgx_runtime_env) = platform_info.pkgx_runtime_env {
            table.insert(
                "pkgx_runtime_env".to_string(),
                toml::Value::Table(toml_table_from_string_map(pkgx_runtime_env)),
            );
        }
        if let Some(ref provenance) = platform_info.provenance {
            match provenance {
                ProvenanceType::Slsa { url: Some(url) } => {
                    let mut slsa_table = toml::Table::new();
                    slsa_table.insert("url".to_string(), url.clone().into());
                    let mut prov_table = toml::Table::new();
                    prov_table.insert("slsa".to_string(), toml::Value::Table(slsa_table));
                    table.insert("provenance".to_string(), toml::Value::Table(prov_table));
                }
                // Slsa without URL and all other variants serialize as plain string
                _ => {
                    table.insert("provenance".to_string(), provenance.to_string().into());
                }
            }
        }
        toml::Value::Table(table)
    }
}

impl TryFrom<toml::Value> for CondaPackageInfo {
    type Error = Report;
    fn try_from(value: toml::Value) -> Result<Self> {
        match value {
            toml::Value::Table(mut t) => {
                let url = t
                    .remove("url")
                    .and_then(|v| match v {
                        toml::Value::String(s) => Some(s),
                        _ => None,
                    })
                    .ok_or_else(|| eyre::eyre!("missing url in conda package info"))?;
                let checksum = match t.remove("checksum") {
                    Some(toml::Value::String(s)) => Some(s),
                    _ => None,
                };
                Ok(CondaPackageInfo { url, checksum })
            }
            _ => bail!("unsupported conda package info format"),
        }
    }
}

impl TryFrom<toml::Value> for PkgxPackageInfo {
    type Error = Report;
    fn try_from(value: toml::Value) -> Result<Self> {
        match value {
            toml::Value::Table(mut t) => {
                let url = t
                    .remove("url")
                    .and_then(|v| match v {
                        toml::Value::String(s) => Some(s),
                        _ => None,
                    })
                    .ok_or_else(|| eyre::eyre!("missing url in pkgx package info"))?;
                let checksum = match t.remove("checksum") {
                    Some(toml::Value::String(s)) => Some(s),
                    _ => None,
                };
                let pkgx_provides = match t.remove("pkgx_provides") {
                    Some(toml::Value::Array(arr)) => Some(
                        arr.into_iter()
                            .filter_map(|v| v.as_str().map(String::from))
                            .collect(),
                    ),
                    _ => None,
                };
                let pkgx_runtime_env = match t.remove("pkgx_runtime_env") {
                    Some(toml::Value::Table(table)) => {
                        Some(string_table_from_toml_value(toml::Value::Table(table))?)
                    }
                    _ => None,
                };
                Ok(PkgxPackageInfo {
                    url,
                    checksum,
                    pkgx_provides,
                    pkgx_runtime_env,
                })
            }
            _ => bail!("unsupported pkgx package info format"),
        }
    }
}

fn string_table_from_toml_value(value: toml::Value) -> Result<BTreeMap<String, String>> {
    match value {
        toml::Value::Table(table) => Ok(table
            .into_iter()
            .filter_map(|(key, value)| match value {
                toml::Value::String(value) => Some((key, value)),
                _ => None,
            })
            .collect()),
        _ => Ok(BTreeMap::new()),
    }
}

fn toml_table_from_string_map(values: BTreeMap<String, String>) -> toml::Table {
    values.into_iter().map(|(k, v)| (k, v.into())).collect()
}

fn existing_lockfile_doc_url(content: &str) -> Option<String> {
    content
        .lines()
        .next()?
        .strip_prefix(LOCKFILE_HEADER_PREFIX)
        .map(str::trim)
        .filter(|url| !url.is_empty())
        .map(str::to_string)
}

fn existing_lockfile_doc_url_from_path(path: &Path) -> Option<String> {
    file::read_to_string(path)
        .ok()
        .and_then(|content| existing_lockfile_doc_url(&content))
}

impl Lockfile {
    pub fn read<P: AsRef<Path>>(path: P) -> Result<Self> {
        let path = path.as_ref();
        if !path.exists() {
            return Ok(Lockfile::default());
        }
        trace!("reading lockfile {}", path.display_user());
        let content = file::read_to_string(path)?;
        let generated_header_url = existing_lockfile_doc_url(&content);
        let mut table: toml::Table = toml::from_str(&content)?;

        let tools: toml::Table = table
            .remove("tools")
            .unwrap_or(toml::Table::new().into())
            .try_into()?;

        let mut lockfile = Lockfile {
            generated_header_url,
            ..Default::default()
        };

        for (short, value) in tools {
            let versions = match value {
                toml::Value::Array(arr) => arr
                    .into_iter()
                    .map(LockfileTool::try_from)
                    .collect::<Result<Vec<_>>>()?,
                _ => bail!(
                    "invalid lockfile format for tool {short}: expected array ([[tools.{short}]])"
                ),
            };
            lockfile.tools.insert(short, versions);
        }

        // Parse conda-packages section: platform -> basename -> CondaPackageInfo
        if let Some(conda_packages) = table.remove("conda-packages") {
            let platforms: toml::Table = conda_packages.try_into()?;
            for (platform, packages) in platforms {
                let packages_table: toml::Table = packages.try_into()?;
                for (basename, info) in packages_table {
                    let info: CondaPackageInfo = info.try_into()?;
                    lockfile
                        .conda_packages
                        .entry(platform.clone())
                        .or_default()
                        .insert(basename, info);
                }
            }
        }

        // Parse pkgx-packages section: platform -> package@version -> PkgxPackageInfo
        if let Some(pkgx_packages) = table.remove("pkgx-packages") {
            let platforms: toml::Table = pkgx_packages.try_into()?;
            for (platform, packages) in platforms {
                let packages_table: toml::Table = packages.try_into()?;
                for (id, info) in packages_table {
                    let info: PkgxPackageInfo = info.try_into()?;
                    lockfile
                        .pkgx_packages
                        .entry(platform.clone())
                        .or_default()
                        .insert(id, info);
                }
            }
        }

        Ok(lockfile)
    }

    fn save<P: AsRef<Path>>(&self, path: P) -> Result<()> {
        let path = path.as_ref();
        let mut lockfile = toml::Table::new();

        // Write conda-packages section first (before tools for nicer ordering)
        if !self.conda_packages.is_empty() {
            let mut conda_packages = toml::Table::new();
            for (platform, packages) in &self.conda_packages {
                let mut platform_table = toml::Table::new();
                for (basename, info) in packages {
                    let mut pkg_table = toml::Table::new();
                    pkg_table.insert("url".to_string(), info.url.clone().into());
                    if let Some(checksum) = &info.checksum {
                        pkg_table.insert("checksum".to_string(), checksum.clone().into());
                    }
                    platform_table.insert(basename.clone(), pkg_table.into());
                }
                conda_packages.insert(platform.clone(), platform_table.into());
            }
            lockfile.insert("conda-packages".to_string(), conda_packages.into());
        }

        if !self.pkgx_packages.is_empty() {
            let mut pkgx_packages = toml::Table::new();
            for (platform, packages) in &self.pkgx_packages {
                let mut platform_table = toml::Table::new();
                for (id, info) in packages {
                    let mut pkg_table = toml::Table::new();
                    pkg_table.insert("url".to_string(), info.url.clone().into());
                    if let Some(checksum) = &info.checksum {
                        pkg_table.insert("checksum".to_string(), checksum.clone().into());
                    }
                    if let Some(provides) = &info.pkgx_provides {
                        pkg_table.insert(
                            "pkgx_provides".to_string(),
                            provides
                                .iter()
                                .cloned()
                                .map(toml::Value::String)
                                .collect::<Vec<_>>()
                                .into(),
                        );
                    }
                    if let Some(runtime_env) = &info.pkgx_runtime_env {
                        pkg_table.insert(
                            "pkgx_runtime_env".to_string(),
                            toml::Value::Table(toml_table_from_string_map(runtime_env.clone())),
                        );
                    }
                    platform_table.insert(id.clone(), pkg_table.into());
                }
                pkgx_packages.insert(platform.clone(), platform_table.into());
            }
            lockfile.insert("pkgx-packages".to_string(), pkgx_packages.into());
        }

        // Write tools section
        let mut tools = toml::Table::new();
        for (short, versions) in &self.tools {
            // Always write Multi-Version format (array format) for consistency
            let value: toml::Value = versions
                .iter()
                .cloned()
                .map(|version| version.into_toml_value())
                .collect::<Vec<toml::Value>>()
                .into();
            tools.insert(short.clone(), value);
        }
        lockfile.insert("tools".to_string(), tools.into());

        let content = toml::to_string_pretty(&toml::Value::Table(lockfile))?;
        let content = format(content.parse()?);
        let doc_url = self
            .generated_header_url
            .clone()
            .or_else(|| existing_lockfile_doc_url_from_path(path))
            .unwrap_or_else(|| DEFAULT_LOCKFILE_DOC_URL.to_string());
        let content = format!("{LOCKFILE_HEADER_PREFIX}{doc_url}\n\n{content}");

        // Resolve the symlink target first, before writing the temp file
        let target = if path.is_symlink() {
            // If the existing lockfile is a symlink, resolve it and update the target instead
            // of replacing the symlink
            trace!(
                "lockfile {} is a symlink, updating target instead of replacing",
                display_path(path)
            );

            match fs::canonicalize(path) {
                Ok(link_target) => {
                    trace!(
                        "resolved lockfile symlink {} to {}",
                        display_path(path),
                        display_path(&link_target)
                    );
                    link_target
                }
                Err(e) => {
                    // Dangling symlink – fall back to overwriting the symlink itself
                    // TODO: Maybe instead of overwriting, we should create the new lockfile at
                    // the symlink's target path?
                    warn!(
                        "lockfile {} is a dangling symlink ({}), overwriting the symlink itself",
                        display_path(path),
                        e
                    );
                    path.to_path_buf()
                }
            }
        } else {
            path.to_path_buf()
        };
        // Use atomic write: write to a uniquely-named temp file, then persist.
        // - Prevents partial writes from corrupting the lockfile.
        // - Unique temp name prevents races when multiple mise processes update
        //   the same lockfile concurrently (e.g. parallel linters each triggering
        //   `install_missing_bin`). A fixed `mise.lock.tmp` collided here and the
        //   loser of the rename race got ENOENT.
        // Write alongside the real target so the rename stays on the same filesystem.
        let parent = target
            .parent()
            .ok_or_else(|| eyre!("lockfile path has no parent: {}", display_path(&target)))?;
        let mut tmp = tempfile::NamedTempFile::with_prefix_in(".mise.lock.", parent)?;
        tmp.as_file_mut().write_all(content.as_bytes())?;
        apply_lockfile_permissions(&tmp, &target)?;
        persist_lockfile_tmp(tmp, &target)?;

        invalidate_caches();
        Ok(())
    }

    /// Add or update a conda package in the shared section
    /// `basename` is without extension, e.g., "ncurses-6.4-h7ea286d_0"
    pub fn set_conda_package(&mut self, platform: &str, basename: &str, info: CondaPackageInfo) {
        self.conda_packages
            .entry(platform.to_string())
            .or_default()
            .insert(basename.to_string(), info);
    }

    /// Get a conda package from the shared section by basename
    pub fn get_conda_package(&self, platform: &str, basename: &str) -> Option<&CondaPackageInfo> {
        self.conda_packages.get(platform)?.get(basename)
    }

    pub fn set_pkgx_package(&mut self, platform: &str, id: &str, info: PkgxPackageInfo) {
        self.pkgx_packages
            .entry(platform.to_string())
            .or_default()
            .insert(id.to_string(), info);
    }

    pub fn get_pkgx_package(&self, platform: &str, id: &str) -> Option<&PkgxPackageInfo> {
        self.pkgx_packages.get(platform)?.get(id)
    }

    /// Remove unreferenced conda packages from the shared section.
    /// A package is unreferenced if no tool's conda_deps references it.
    fn cleanup_unreferenced_conda_packages(&mut self) {
        // Collect all referenced conda packages (platform -> set of basenames)
        let mut referenced: HashMap<String, HashSet<String>> = HashMap::new();
        for tools in self.tools.values() {
            for tool in tools {
                for (platform, info) in &tool.platforms {
                    if let Some(deps) = &info.conda_deps {
                        for dep in deps {
                            referenced
                                .entry(platform.clone())
                                .or_default()
                                .insert(dep.clone());
                        }
                    }
                }
            }
        }

        // Remove unreferenced packages
        for (platform, packages) in &mut self.conda_packages {
            let referenced_for_platform = referenced.get(platform);
            packages.retain(|basename, _| {
                referenced_for_platform
                    .map(|refs| refs.contains(basename))
                    .unwrap_or(false)
            });
        }

        // Remove empty platform entries
        self.conda_packages
            .retain(|_, packages| !packages.is_empty());
    }

    fn cleanup_unreferenced_pkgx_packages(&mut self) {
        let mut referenced: HashMap<String, HashSet<String>> = HashMap::new();
        for tools in self.tools.values() {
            for tool in tools {
                for (platform, info) in &tool.platforms {
                    if let Some(deps) = &info.pkgx_deps {
                        for dep in deps {
                            referenced
                                .entry(platform.clone())
                                .or_default()
                                .insert(dep.clone());
                        }
                    }
                }
            }
        }

        for (platform, packages) in &mut self.pkgx_packages {
            let referenced_for_platform = referenced.get(platform);
            packages.retain(|id, _| {
                referenced_for_platform
                    .map(|refs| refs.contains(id))
                    .unwrap_or(false)
            });
        }

        self.pkgx_packages
            .retain(|_, packages| !packages.is_empty());
    }

    /// Get all platform keys present in the lockfile
    pub fn all_platform_keys(&self) -> BTreeSet<String> {
        let mut platforms = BTreeSet::new();
        for tools in self.tools.values() {
            for tool in tools {
                for platform_key in tool.platforms.keys() {
                    platforms.insert(platform_key.clone());
                }
            }
        }
        platforms
    }

    pub fn tools(&self) -> &BTreeMap<String, Vec<LockfileTool>> {
        &self.tools
    }

    /// Keep only tools matching configured short names or backend identifiers.
    /// Also removes conda packages that become unreferenced.
    pub fn retain_tools_by_short_or_backend(
        &mut self,
        keep_shorts: &BTreeSet<String>,
        keep_backends: &BTreeSet<String>,
    ) {
        self.tools.retain(|short, versions| {
            Self::should_keep_tool(short, versions, keep_shorts, keep_backends)
        });
        self.cleanup_unreferenced_conda_packages();
        self.cleanup_unreferenced_pkgx_packages();
    }

    /// Remove entries for a tool whose version is not in the given set.
    /// Used to prune stale version entries during filtered `mise lock <tool>` runs.
    pub fn retain_tool_versions(&mut self, short: &str, keep_versions: &BTreeSet<String>) {
        if let Some(tools) = self.tools.get_mut(short) {
            tools.retain(|t| keep_versions.contains(&t.version));
            if tools.is_empty() {
                self.tools.remove(short);
            }
        }
        self.cleanup_unreferenced_conda_packages();
        self.cleanup_unreferenced_pkgx_packages();
    }

    /// Return versions of a tool that would be removed by `retain_tool_versions`.
    pub fn stale_tool_versions(
        &self,
        short: &str,
        keep_versions: &BTreeSet<String>,
    ) -> Vec<String> {
        self.tools
            .get(short)
            .map(|tools| {
                tools
                    .iter()
                    .filter(|t| !keep_versions.contains(&t.version))
                    .map(|t| t.version.clone())
                    .collect()
            })
            .unwrap_or_default()
    }

    /// Return tool keys that would be removed by `retain_tools_by_short_or_backend`.
    pub fn stale_tool_shorts(
        &self,
        keep_shorts: &BTreeSet<String>,
        keep_backends: &BTreeSet<String>,
    ) -> BTreeSet<String> {
        self.tools
            .iter()
            .filter_map(|(short, versions)| {
                (!Self::should_keep_tool(short, versions, keep_shorts, keep_backends))
                    .then_some(short.clone())
            })
            .collect()
    }

    fn should_keep_tool(
        short: &str,
        versions: &[LockfileTool],
        keep_shorts: &BTreeSet<String>,
        keep_backends: &BTreeSet<String>,
    ) -> bool {
        keep_shorts.contains(short)
            || versions
                .iter()
                .filter_map(|v| v.backend.as_ref())
                .any(|backend| keep_backends.contains(backend))
    }

    /// Update or add platform info for a tool version
    /// Merges with existing info, preserving fields we don't have new values for
    pub fn set_platform_info(
        &mut self,
        short: &str,
        version: &str,
        backend: Option<&str>,
        options: &BTreeMap<String, String>,
        platform_key: &str,
        platform_info: PlatformInfo,
    ) {
        let tools = self.tools.entry(short.to_string()).or_default();
        // Platform option migration is handled while merging freshly resolved
        // entries. Writes here always target the exact resolved option variant.
        let idx = tools
            .iter()
            .position(|t| t.version == version && &t.options == options);
        if let Some(idx) = idx {
            let tool = &mut tools[idx];
            // Merge with existing platform info, preferring new values when present.
            // When the URL changes, drop existing checksum/size/url_api — those fields
            // describe a specific artifact and are stale once the URL points elsewhere.
            let merged = if let Some(existing) = tool.platforms.get(platform_key) {
                let url_changed = platform_info.url.is_some()
                    && existing.url.is_some()
                    && platform_info.url != existing.url;
                let install_changed =
                    platform_info.install.is_some() && platform_info.install != existing.install;
                let source_to_url = platform_info.url.is_some()
                    && platform_info.install.is_none()
                    && existing.install.as_deref() == Some("source");
                let preserve_artifact_fields = !url_changed && !install_changed && !source_to_url;
                let provenance = if preserve_artifact_fields {
                    match (
                        platform_info.provenance.clone(),
                        existing.provenance.clone(),
                    ) {
                        (Some(a), Some(b)) => Some(a.merge(b)),
                        (a, b) => a.or(b),
                    }
                } else {
                    platform_info.provenance.clone()
                };
                PlatformInfo {
                    install: platform_info.install.or_else(|| {
                        if platform_info.url.is_some() {
                            None
                        } else {
                            existing.install.clone()
                        }
                    }),
                    checksum: platform_info.checksum.or_else(|| {
                        if preserve_artifact_fields {
                            existing.checksum.clone()
                        } else {
                            None
                        }
                    }),
                    size: platform_info.size.or(if preserve_artifact_fields {
                        existing.size
                    } else {
                        None
                    }),
                    url: platform_info.url.or_else(|| {
                        if preserve_artifact_fields {
                            existing.url.clone()
                        } else {
                            None
                        }
                    }),
                    url_api: platform_info.url_api.or_else(|| {
                        if preserve_artifact_fields {
                            existing.url_api.clone()
                        } else {
                            None
                        }
                    }),
                    // For dependency lists, always use the new value - None means "no dependencies"
                    // rather than "not computed", so we shouldn't preserve stale deps
                    conda_deps: platform_info.conda_deps,
                    pkgx_deps: platform_info.pkgx_deps,
                    pkgx_provides: platform_info.pkgx_provides,
                    pkgx_runtime_env: platform_info.pkgx_runtime_env,
                    provenance,
                    github_attestations: None,
                }
            } else {
                platform_info
            };
            // Only insert non-empty platform info to avoid `"platforms.linux-x64" = {}`
            if !merged.is_empty() {
                tool.platforms.insert(platform_key.to_string(), merged);
            }
        } else {
            let mut platforms = BTreeMap::new();
            // Only insert non-empty platform info
            if !platform_info.is_empty() {
                platforms.insert(platform_key.to_string(), platform_info);
            }
            tools.push(LockfileTool {
                version: version.to_string(),
                backend: backend.map(|s| s.to_string()),
                options: options.clone(),
                platforms,
            });
        }

        // Do not remove this platform from sibling option variants. A monorepo
        // root lockfile can legitimately contain the same version and platform
        // for multiple requests with different options. Legacy migration is
        // handled by the consensus-based merge pass, where all contributing
        // requests are available.
    }

    /// Save the lockfile to disk (public for mise lock command)
    pub fn write<P: AsRef<Path>>(&self, path: P) -> Result<()> {
        self.save(path)
    }
}

/// Determines the lockfile path for a given config file path
/// Returns (lockfile_path, is_local)
///
/// Lockfiles are placed alongside their config files:
/// - `mise.toml` -> `mise.lock`
/// - `.config/mise.toml` -> `.config/mise.lock`
/// - `.mise/config.toml` -> `.mise/mise.lock`
/// - `.mise/conf.d/foo.toml` -> `.mise/mise.lock` (conf.d files share parent's lockfile)
pub fn lockfile_path_for_config(
    config_path: &Path,
    monorepo_root: Option<&Path>,
) -> (PathBuf, bool) {
    let is_local = is_local_config(config_path);
    let env = extract_env_from_config_path(config_path);
    let lockfile_name = match (&env, is_local) {
        (Some(e), true) => format!("mise.{e}.local.lock"),
        (Some(e), false) => format!("mise.{e}.lock"),
        (None, true) => "mise.local.lock".to_string(),
        (None, false) => "mise.lock".to_string(),
    };

    let parent = config_path.parent().unwrap_or(Path::new("."));
    let parent_name = parent
        .file_name()
        .and_then(|n| n.to_str())
        .unwrap_or_default();

    // For conf.d files, place lockfile at parent of conf.d so all conf.d files share one lockfile
    let lockfile_dir = if parent_name == "conf.d" {
        parent.parent().unwrap_or(parent)
    } else {
        parent
    };

    let lockfile_dir = if let Some(root) = monorepo_root
        && config_path.starts_with(root)
        && lockfile_dir != root
    {
        root
    } else {
        lockfile_dir
    };

    (lockfile_dir.join(lockfile_name), is_local)
}

/// Determines the lockfile path for a tool source.
///
/// Idiomatic version files are not config files themselves, so their lock entries
/// belong to the nearest active mise config root that contains the version file.
/// If multiple base configs share that root, the later entry in config_files wins
/// so colocated configs have a deterministic lockfile target, such as
/// .mise/config.toml mapping .dummy-version to .mise/mise.lock instead of mise.lock.
pub fn lockfile_path_for_tool_source(
    config: &Config,
    source: &ToolSource,
) -> Option<(PathBuf, bool)> {
    let monorepo_root = config.monorepo_lockfile_root();
    lockfile_path_for_tool_source_with_root(config, source, monorepo_root.as_deref())
}

fn lockfile_path_for_tool_source_with_root(
    config: &Config,
    source: &ToolSource,
    monorepo_root: Option<&Path>,
) -> Option<(PathBuf, bool)> {
    match source {
        ToolSource::MiseToml(path) => Some(lockfile_path_for_config(path, monorepo_root)),
        ToolSource::IdiomaticVersionFile(path) => config
            .config_files
            .iter()
            .enumerate()
            .filter(|(_, (_, cf))| cf.source().is_mise_toml())
            .filter_map(|(idx, (config_path, cf))| {
                let root = cf.project_root().unwrap_or_else(|| cf.config_root());
                let is_base = !is_local_config(config_path)
                    && extract_env_from_config_path(config_path).is_none();
                path.starts_with(&root).then(|| {
                    (
                        root.components().count(),
                        is_base,
                        // Tie-break same-root base configs by config_files order.
                        idx,
                        lockfile_path_for_config(config_path, monorepo_root),
                    )
                })
            })
            .max_by_key(|(root_depth, is_base, idx, _)| (*root_depth, *is_base, *idx))
            .map(|(_, _, _, lockfile)| lockfile),
        _ => None,
    }
}

/// Checks if a config path is a "local" config (should go to mise.local.lock)
fn is_local_config(path: &Path) -> bool {
    let filename = path
        .file_name()
        .and_then(|n| n.to_str())
        .unwrap_or_default();
    filename.contains(".local.")
}

/// Extracts environment name from config filename
/// e.g., "mise.test.toml" -> Some("test"), "mise.test.local.toml" -> Some("test"), "mise.toml" -> None
pub fn extract_env_from_config_path(path: &Path) -> Option<String> {
    let filename = path
        .file_name()
        .and_then(|n| n.to_str())
        .unwrap_or_default();

    // Pattern matches:
    // - mise.{env}.toml -> captures env
    // - mise.{env}.local.toml -> captures env (env-specific local config)
    // - .mise.{env}.toml -> captures env
    // - config.{env}.toml -> captures env
    // Does NOT match (returns None):
    // - mise.toml, .mise.toml, config.toml (base configs)
    // - mise.local.toml (local without env - filtered by "local" check)
    let re = regex!(r"^(?:\.?mise|config)\.([^.]+)(?:\.local)?\.toml$");
    re.captures(filename)
        .and_then(|caps| caps.get(1))
        .map(|m| m.as_str().to_string())
        .filter(|s| s != "local")
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum LockfileUpdateMode {
    Normal,
    AllowLocked,
}

impl LockfileUpdateMode {
    fn allow_locked(self) -> bool {
        matches!(self, Self::AllowLocked)
    }
}

pub fn migrate_monorepo_lockfiles(config: &Config) -> Result<()> {
    if !Settings::get().lockfile_enabled() {
        return Ok(());
    }
    let Some(monorepo_root) = config.monorepo_lockfile_root() else {
        return Ok(());
    };
    let mut migrated = 0usize;

    for source in monorepo_legacy_lockfile_paths(config) {
        if !source.exists() {
            continue;
        }
        let Some(filename) = source.file_name() else {
            continue;
        };
        let target = monorepo_root.join(filename);
        if source == target {
            continue;
        }
        let _lock = crate::lock_file::LockFile::new(&target)
            .with_callback(|l| debug!("waiting for lock on {}", display_path(l)))
            .lock()?;
        let mut root_lockfile =
            Lockfile::read(&target).unwrap_or_else(|err| handle_lockfile_read_error(err, &target));
        let subproject_lockfile = match Lockfile::read(&source) {
            Ok(lockfile) => lockfile,
            Err(err) if is_not_found_report(&err) => continue,
            Err(err) => return Err(err),
        };
        merge_lockfile_preserving_root(&mut root_lockfile, subproject_lockfile);
        root_lockfile.save(&target)?;
        if let Err(err) = fs::remove_file(&source)
            && err.kind() != ErrorKind::NotFound
        {
            return Err(err.into());
        }
        migrated += 1;
    }

    if migrated > 0 {
        miseprintln!(
            "migrated {migrated} subproject lockfile(s) into {} (monorepo uses a single root lockfile)",
            display_path(&monorepo_root)
        );
    }

    Ok(())
}

fn is_not_found_report(err: &Report) -> bool {
    err.downcast_ref::<std::io::Error>()
        .is_some_and(|err| err.kind() == ErrorKind::NotFound)
}

fn monorepo_legacy_lockfile_paths(config: &Config) -> IndexSet<PathBuf> {
    let Some(monorepo_root) = config.monorepo_lockfile_root() else {
        return IndexSet::new();
    };
    let Ok(config_roots) = config.monorepo_config_root_dirs_for_lockfiles() else {
        return IndexSet::new();
    };
    let mut source_lockfiles = IndexSet::new();

    for config_root in config_roots {
        if config_root == monorepo_root {
            continue;
        }
        for source in lockfile_variant_paths_in_dir(&config_root) {
            if source.parent() != Some(monorepo_root.as_path()) {
                source_lockfiles.insert(source);
            }
        }
        for config_path in crate::config::config_paths_in_dir(&config_root) {
            if let Some(source) =
                legacy_lockfile_path_for_config(config, &config_path, &monorepo_root)
            {
                source_lockfiles.insert(source);
            }
        }
    }

    source_lockfiles
}

pub(crate) fn lockfile_variant_paths_in_dir(dir: &Path) -> Vec<PathBuf> {
    let mut paths = IndexSet::new();

    for path in crate::config::glob(dir, "mise*.lock").unwrap_or_default() {
        paths.insert(path);
    }
    for env_name in env::MISE_ENV.iter().chain(env::AUTO_ENV_NAMES.iter()) {
        paths.insert(dir.join(format!("mise.{env_name}.local.lock")));
        paths.insert(dir.join(format!("mise.{env_name}.lock")));
    }
    paths.insert(dir.join("mise.local.lock"));
    paths.insert(dir.join("mise.lock"));

    paths.into_iter().collect()
}

fn legacy_lockfile_path_for_config(
    config: &Config,
    config_path: &Path,
    monorepo_root: &Path,
) -> Option<PathBuf> {
    if !config_path.starts_with(monorepo_root) || config.monorepo_lockfile_root().is_none() {
        return None;
    }
    let (source, _) = lockfile_path_for_config(config_path, None);
    (source.parent() != Some(monorepo_root)).then_some(source)
}

fn merge_legacy_lockfile_if_present(lockfile: &mut Lockfile, legacy_path: &Path) -> Result<()> {
    match Lockfile::read(legacy_path) {
        Ok(legacy) => {
            merge_lockfile_preserving_root(lockfile, legacy);
            Ok(())
        }
        Err(err) if is_not_found_report(&err) => Ok(()),
        Err(err) => Err(err),
    }
}

fn merge_lockfile_preserving_root(root: &mut Lockfile, other: Lockfile) {
    for (short, tools) in other.tools {
        let root_tools = root.tools.entry(short).or_default();
        let mut keys: HashSet<(String, BTreeMap<String, String>)> = root_tools
            .iter()
            .map(|tool| (tool.version.clone(), tool.options.clone()))
            .collect();
        for tool in tools {
            let key = (tool.version.clone(), tool.options.clone());
            if keys.insert(key) {
                root_tools.push(tool);
            }
        }
    }

    for (platform, packages) in other.conda_packages {
        let root_packages = root.conda_packages.entry(platform).or_default();
        for (basename, info) in packages {
            root_packages.entry(basename).or_insert(info);
        }
    }

    for (platform, packages) in other.pkgx_packages {
        let root_packages = root.pkgx_packages.entry(platform).or_default();
        for (id, info) in packages {
            root_packages.entry(id).or_insert(info);
        }
    }
}

pub fn update_lockfiles(
    config: &Config,
    ts: &Toolset,
    new_versions: &[ToolVersion],
    mode: LockfileUpdateMode,
) -> Result<()> {
    if !Settings::get().lockfile_enabled() || (Settings::get().locked && !mode.allow_locked()) {
        return Ok(());
    }

    // Collect tools by source (config file)
    let mut tools_by_source: HashMap<ToolSource, HashMap<String, Vec<ToolVersion>>> =
        HashMap::new();
    for (ba, tvl) in &ts.versions {
        for tv in &tvl.versions {
            tools_by_source
                .entry(tv.request.source().clone())
                .or_default()
                .entry(ba.short.to_string())
                .or_default()
                .push(tv.clone());
        }
    }

    // Add versions added within this session (from `mise use` or `mise up`)
    for (backend, group) in &new_versions.iter().chunk_by(|tv| tv.ba()) {
        let tvs = group.cloned().collect_vec();
        let source = tvs[0].request.source().clone();
        let source_tools = tools_by_source.entry(source.clone()).or_default();

        let existing_versions = source_tools.entry(backend.short.to_string()).or_default();
        for new_tv in tvs {
            existing_versions.retain(|tv| tv.request.version() != new_tv.request.version());
            existing_versions.push(new_tv);
        }
    }

    // Group config files by target lockfile path
    let mut lockfile_configs: HashMap<PathBuf, Vec<PathBuf>> = HashMap::new();
    for (config_path, cf) in config.config_files.iter().rev() {
        if !cf.source().is_mise_toml() {
            continue;
        }
        let (lockfile_path, _is_local) =
            lockfile_path_for_config(config_path, config.monorepo_lockfile_root().as_deref());
        lockfile_configs
            .entry(lockfile_path)
            .or_default()
            .push(config_path.clone());
    }
    for source in tools_by_source.keys() {
        if let Some((lockfile_path, _)) = lockfile_path_for_tool_source(config, source) {
            lockfile_configs.entry(lockfile_path).or_default();
        }
    }

    debug!("updating {} lockfiles", lockfile_configs.len());

    // Process each lockfile, deferring provenance errors until all lockfiles are saved.
    let mut provenance_errors: Vec<String> = Vec::new();

    for (lockfile_path, _configs) in lockfile_configs {
        // Only update existing lockfiles - creation is done elsewhere (e.g., by `mise lock`)
        if !lockfile_path.exists() {
            continue;
        }
        let is_monorepo_root_lockfile = config
            .monorepo_lockfile_root()
            .is_some_and(|root| lockfile_path.parent() == Some(root.as_path()));
        let _lock = crate::lock_file::LockFile::new(&lockfile_path)
            .with_callback(|l| debug!("waiting for lock on {}", display_path(l)))
            .lock()?;

        let mut existing_lockfile = Lockfile::read(&lockfile_path)
            .unwrap_or_else(|err| handle_lockfile_read_error(err, &lockfile_path));

        // Collect all tools from all contributing configs. This starts from the
        // resolved toolset, then overlays newly installed versions because a
        // fuzzy request may still resolve through the old lockfile entry until
        // this update is written.
        let mut tool_versions_by_short: HashMap<String, Vec<ToolVersion>> = HashMap::new();
        let mut contributing_sources = 0;

        for (source, tools) in &tools_by_source {
            let Some((source_lockfile, _)) = lockfile_path_for_tool_source(config, source) else {
                continue;
            };
            if source_lockfile == lockfile_path {
                contributing_sources += 1;
                for (short, versions) in tools {
                    tool_versions_by_short
                        .entry(short.clone())
                        .or_default()
                        .extend(versions.clone());
                }
            }
        }

        trace!(
            "updating lockfile {} from {} source(s)",
            display_path(&lockfile_path),
            contributing_sources
        );

        for new_version in new_versions {
            if let Some((source_lockfile, _)) =
                lockfile_path_for_tool_source(config, new_version.request.source())
            {
                if source_lockfile != lockfile_path {
                    continue;
                }

                let versions = tool_versions_by_short
                    .entry(new_version.short().to_string())
                    .or_default();
                versions.retain(|tv| {
                    tv.ba() != new_version.ba()
                        || tv.request.version() != new_version.request.version()
                        || tv.request.source() != new_version.request.source()
                });
                versions.push(new_version.clone());
            } else if let Some(versions) = tool_versions_by_short.get_mut(new_version.short()) {
                if let Some((idx, request)) = versions
                    .iter()
                    .enumerate()
                    .exactly_one()
                    .ok()
                    .map(|(idx, tv)| (idx, tv.request.clone()))
                {
                    // Only propagate when the new install resolves the same
                    // version specifier as the config. `mise x node` (no
                    // version) is rewritten by `with_default_to_latest` to
                    // carry the config's version string, so it matches and
                    // updates the lockfile as expected. `mise x node@latest`
                    // with mise.toml `node = "24"` carries a "latest"
                    // specifier that doesn't match — that's an ad-hoc CLI
                    // override and pairing the "24" request with a 25.x
                    // install would produce a nonsensical lockfile entry.
                    if new_version.request.version() != request.version() {
                        trace!(
                            "skipping lockfile update for {}@{} in {}: CLI override does not match config request {}",
                            new_version.short(),
                            new_version.version,
                            display_path(&lockfile_path),
                            request.version(),
                        );
                        continue;
                    }
                    let mut new_version = new_version.clone();
                    new_version.request = request;
                    versions.remove(idx);
                    versions.push(new_version);
                } else {
                    trace!(
                        "skipping lockfile update for {}@{} in {}: ambiguous config entries",
                        new_version.short(),
                        new_version.version,
                        display_path(&lockfile_path)
                    );
                }
            }
        }

        let tools_by_short: HashMap<String, Vec<LockfileTool>> = tool_versions_by_short
            .iter()
            .map(|(short, versions)| {
                Ok((
                    short.clone(),
                    versions
                        .iter()
                        .map(lockfile_tool_from_tool_version)
                        .collect::<Result<Vec<_>>>()?,
                ))
            })
            .collect::<Result<_>>()?;

        // Check for provenance regression before merging (which drops old version entries).
        // For github backend tools, error if the highest prior version had provenance but
        // the new version does not — this could indicate a supply chain attack.
        // Regressing tools are excluded from merge to preserve the old provenance entry.
        let (regressing_tools, regression_errors) =
            check_provenance_regression(&existing_lockfile, &tools_by_short);
        provenance_errors.extend(regression_errors);

        // Process each tool with deduplication, skipping regressing tools
        for (short, entries) in tools_by_short {
            if regressing_tools.contains(&short) {
                continue;
            }
            let versions = &tool_versions_by_short[&short];
            // Normal monorepo installs load only the current config hierarchy,
            // not every sibling project. Consensus among those visible requests
            // is therefore not authoritative for the shared root lockfile. Fail
            // closed there and preserve legacy entries; ordinary lockfiles can
            // safely migrate when all requests for the file agree.
            let rekey_decisions = if is_monorepo_root_lockfile {
                RekeyDecisions::new()
            } else {
                build_rekey_decisions(versions, existing_lockfile.tools.get(&short))
            };
            let (mut merged_tools, consumed_keys) = merge_tool_entries(
                entries,
                existing_lockfile.tools.get(&short),
                |version, platform| {
                    rekey_decisions
                        .get(&(version.to_string(), platform.to_key()))
                        .cloned()
                        .flatten()
                },
            );
            if is_monorepo_root_lockfile {
                preserve_absent_tool_entries(
                    &mut merged_tools,
                    existing_lockfile.tools.get(&short),
                    &consumed_keys,
                );
            }
            existing_lockfile.tools.insert(short, merged_tools);
        }

        // Merge conda packages from new_versions into the lockfile
        for tv in new_versions {
            for ((platform, basename), pkg_info) in &tv.conda_packages {
                existing_lockfile.set_conda_package(platform, basename, pkg_info.clone());
            }
            for ((platform, id), pkg_info) in &tv.pkgx_packages {
                existing_lockfile.set_pkgx_package(platform, id, pkg_info.clone());
            }
        }

        // Clean up any conda packages that are no longer referenced by any tool
        existing_lockfile.cleanup_unreferenced_conda_packages();
        existing_lockfile.cleanup_unreferenced_pkgx_packages();

        existing_lockfile.save(&lockfile_path)?;
    }

    // Return all provenance errors after all lockfiles have been saved
    if !provenance_errors.is_empty() {
        return Err(eyre!("{}", provenance_errors.join("\n")));
    }

    Ok(())
}

/// Check if a single tool version is losing provenance relative to prior lockfile entries.
///
/// Returns an error message if a github backend tool upgrade loses provenance,
/// or None if the check passes. Used by both `check_provenance_regression` and
/// `apply_lock_result` to avoid duplicating the detection logic.
fn check_single_tool_provenance(
    existing_tools: Option<&Vec<LockfileTool>>,
    short: &str,
    version: &str,
    backend: &str,
    platform_key: &str,
    new_provenance: Option<&ProvenanceType>,
) -> Option<String> {
    if new_provenance.is_some() || !backend.starts_with("github:") {
        return None;
    }

    let tools = existing_tools?;
    let new_version = parse_provenance_version(version)?;

    // Find the highest prior github semver version with provenance on this platform.
    // Non-semver tags are opaque to mise, so do not guess whether they are upgrades.
    let prior = tools
        .iter()
        .filter(|t| t.version != version)
        .filter(|t| t.backend.as_ref().is_some_and(|b| b.starts_with("github:")))
        .filter(|t| {
            t.platforms
                .get(platform_key)
                .is_some_and(|pi| pi.provenance.is_some())
        })
        .filter_map(|t| Some((parse_provenance_version(&t.version)?, t)))
        .max_by(|(a, _), (b, _)| a.cmp(b))?;

    let (prior_version, prior) = prior;

    // Only flag upgrades — intentional downgrades are allowed
    if new_version <= prior_version {
        return None;
    }

    let prov = prior.platforms[platform_key].provenance.as_ref().unwrap();
    Some(format!(
        "{short}@{version} has no provenance verification on {platform_key}, \
         but {short}@{} had {prov}. This could indicate a supply chain \
         attack. Verify the release is authentic before proceeding.",
        prior.version,
    ))
}

fn parse_provenance_version(version: &str) -> Option<nodejs_semver::Version> {
    nodejs_semver::Version::parse(strip_leading_v(version)).ok()
}

/// Check if any github backend tool is losing provenance when upgrading versions.
///
/// Only checks the current platform because new `LockfileTool` entries (from
/// `ToolVersionList`) only have `lock_platforms` populated for the platform where
/// installation ran. Checking other platforms would produce false positives.
///
/// Returns the set of regressing tool short names and their error messages.
/// Regressing tools should be excluded from merge/save to preserve the old
/// provenance-verified entry in the lockfile.
fn check_provenance_regression(
    existing_lockfile: &Lockfile,
    new_tools: &HashMap<String, Vec<LockfileTool>>,
) -> (HashSet<String>, Vec<String>) {
    let current_platform = Platform::current().to_key();
    let mut regressing = HashSet::new();
    let mut errors = Vec::new();

    for (short, new_entries) in new_tools {
        for new_entry in new_entries {
            let backend = new_entry.backend.as_deref().unwrap_or("");
            let new_provenance = new_entry
                .platforms
                .get(&current_platform)
                .and_then(|pi| pi.provenance.as_ref());

            if let Some(err) = check_single_tool_provenance(
                existing_lockfile.tools.get(short),
                short,
                &new_entry.version,
                backend,
                &current_platform,
                new_provenance,
            ) {
                regressing.insert(short.clone());
                errors.push(err);
            }
        }
    }
    (regressing, errors)
}

/// Snapshot the platform keys present in each lockfile that the upcoming install run
/// will touch. Must be called BEFORE `update_lockfiles` writes any current-platform
/// entries — the snapshot is what lets `auto_lock_new_versions` tell a user-curated
/// lockfile (existing entries are authoritative) apart from a fresh one whose only
/// current-platform entry was just added by this install.
pub fn snapshot_pre_install_platforms(
    config: &Config,
    ts: &Toolset,
    new_versions: &[ToolVersion],
) -> HashMap<PathBuf, BTreeSet<String>> {
    let mut result: HashMap<PathBuf, BTreeSet<String>> = HashMap::new();
    for tv in ts
        .versions
        .values()
        .flat_map(|tvl| tvl.versions.iter())
        .chain(new_versions.iter())
    {
        let Some((lockfile_path, _)) = lockfile_path_for_tool_source(config, tv.request.source())
        else {
            continue;
        };
        if result.contains_key(&lockfile_path) {
            continue;
        }
        let keys = if lockfile_path.exists() {
            Lockfile::read(&lockfile_path)
                .map(|lf| lf.all_platform_keys())
                .unwrap_or_default()
        } else {
            BTreeSet::new()
        };
        result.insert(lockfile_path, keys);
    }
    result
}

fn tool_version_needs_auto_lock(
    lockfile: &Lockfile,
    tv: &ToolVersion,
    target_platforms: &[Platform],
) -> Result<bool> {
    let Some(tools) = lockfile.tools.get(tv.short()) else {
        return Ok(false);
    };
    // The toolset passed to auto-locking can still contain the version that was
    // active before an upgrade. If that version has already been pruned from the
    // lockfile, it is stale and must not be resolved back into the file.
    if !tools.iter().any(|tool| tool.version == tv.version) {
        return Ok(false);
    }
    let backend = tv.request.backend()?;

    for platform in target_platforms {
        for variant in backend.platform_variants(platform) {
            let target = PlatformTarget::new(variant.clone());
            let options = backend.resolve_lockfile_options(&tv.request, &target)?;
            let platform_key = variant.to_key();
            if lockfile_target_needs_auto_lock(tools, &tv.version, &platform_key, &options) {
                return Ok(true);
            }
        }
    }

    Ok(false)
}

fn lockfile_target_needs_auto_lock(
    tools: &[LockfileTool],
    version: &str,
    platform_key: &str,
    options: &BTreeMap<String, String>,
) -> bool {
    let Some(tool) = tools
        .iter()
        .find(|tool| tool.version == version && &tool.options == options)
    else {
        return true;
    };
    !tool
        .platforms
        .get(platform_key)
        .is_some_and(|info| info.checksum.is_some() && info.url.is_some())
}

/// Determine target platforms for auto-lock from the pre-install lockfile snapshot.
///
/// If the lockfile had platform entries before this install run, those are authoritative
/// (plus the current platform). Otherwise — the lockfile was fresh — fall back to the
/// common platform set so a brand-new lockfile gets cross-platform-populated. The
/// `lockfile_platforms` setting overrides both paths.
fn determine_target_platforms_from_lockfile(
    pre_install_keys: &BTreeSet<String>,
) -> Result<Vec<Platform>> {
    if let Some(configured) = Settings::get().lockfile_platforms()? {
        let mut platforms: BTreeSet<Platform> = configured.into_iter().collect();
        platforms.insert(Platform::current());
        return Ok(platforms.into_iter().collect());
    }

    if !pre_install_keys.is_empty() {
        let mut platforms: BTreeSet<Platform> = BTreeSet::new();
        for platform_key in pre_install_keys {
            if let Ok(p) = Platform::parse(platform_key)
                && p.validate().is_ok()
            {
                platforms.insert(p);
            }
        }
        if !platforms.is_empty() {
            platforms.insert(Platform::current());
            return Ok(platforms.into_iter().collect());
        }
    }

    // Fresh lockfile (or no parseable keys) — populate common defaults + current.
    let mut platforms: BTreeSet<Platform> = Platform::common_platforms().into_iter().collect();
    platforms.insert(Platform::current());
    Ok(platforms.into_iter().collect())
}

/// Determine target platforms from an existing lockfile for explicit `mise lock` calls.
/// If the lockfile already has platform entries, only those are targeted.
/// Otherwise, falls back to all common platforms + current platform.
pub fn determine_existing_platforms(lockfile_path: &Path) -> Result<Vec<Platform>> {
    // If lockfile_platforms setting is configured, use it as the authoritative set
    if let Some(configured) = Settings::get().lockfile_platforms()? {
        let mut platforms: BTreeSet<Platform> = configured.into_iter().collect();
        platforms.insert(Platform::current());
        return Ok(platforms.into_iter().collect());
    }

    if let Ok(lockfile) = Lockfile::read(lockfile_path) {
        let existing_keys = lockfile.all_platform_keys();
        if !existing_keys.is_empty() {
            let mut platforms: BTreeSet<Platform> = BTreeSet::new();
            for platform_key in existing_keys {
                if let Ok(p) = Platform::parse(&platform_key)
                    && p.validate().is_ok()
                {
                    platforms.insert(p);
                }
            }
            if !platforms.is_empty() {
                return Ok(platforms.into_iter().collect());
            }
        }
    }
    // No lockfile, no platforms yet, or no valid platform keys — use common defaults
    let mut platforms: BTreeSet<Platform> = Platform::common_platforms().into_iter().collect();
    platforms.insert(Platform::current());
    Ok(platforms.into_iter().collect())
}

/// After installing new tool versions, resolve checksums/URLs for all common platforms
/// so the lockfile is complete and doesn't change when other developers on different
/// platforms run `mise install`.
pub async fn auto_lock_new_versions(
    config: &Config,
    ts: &Toolset,
    new_versions: &[ToolVersion],
    pre_install_platforms: &HashMap<PathBuf, BTreeSet<String>>,
    mode: LockfileUpdateMode,
) -> Result<()> {
    if !Settings::get().lockfile_enabled()
        || (Settings::get().locked && !mode.allow_locked())
        || new_versions.is_empty()
    {
        return Ok(());
    }

    let settings = Settings::get();
    let jobs = settings.jobs;
    let mut all_provenance_errors: Vec<String> = Vec::new();

    let empty_keys: BTreeSet<String> = BTreeSet::new();
    let mut candidate_versions_by_lockfile: HashMap<PathBuf, Vec<ToolVersion>> = HashMap::new();
    let mut seen_candidates: HashMap<PathBuf, HashSet<(String, String, String, ToolSource)>> =
        HashMap::new();
    for tv in ts
        .versions
        .values()
        .flat_map(|tvl| tvl.versions.iter())
        .chain(new_versions.iter())
    {
        let Ok(backend) = tv.request.backend() else {
            continue;
        };
        if !backend.supports_lockfile_url() {
            continue;
        }
        if let Some((lockfile_path, _)) = lockfile_path_for_tool_source(config, tv.request.source())
        {
            let candidate_key = (
                tv.ba().short.clone(),
                tv.version.clone(),
                tv.request.version().to_string(),
                tv.request.source().clone(),
            );
            if seen_candidates
                .entry(lockfile_path.clone())
                .or_default()
                .insert(candidate_key)
            {
                candidate_versions_by_lockfile
                    .entry(lockfile_path)
                    .or_default()
                    .push(tv.clone());
            }
        }
    }

    for (lockfile_path, versions) in candidate_versions_by_lockfile {
        // Only update existing lockfiles (consistent with update_lockfiles)
        if !lockfile_path.exists() {
            continue;
        }

        let _lock = crate::lock_file::LockFile::new(&lockfile_path)
            .with_callback(|l| debug!("waiting for lock on {}", display_path(l)))
            .lock()?;
        let mut lockfile = Lockfile::read(&lockfile_path)
            .unwrap_or_else(|err| handle_lockfile_read_error(err, &lockfile_path));

        let pre_install_keys = pre_install_platforms
            .get(&lockfile_path)
            .unwrap_or(&empty_keys);
        let target_platforms = determine_target_platforms_from_lockfile(pre_install_keys)?;

        let versions: Vec<_> = versions
            .into_iter()
            .filter(|tv| {
                tool_version_needs_auto_lock(&lockfile, tv, &target_platforms).unwrap_or_else(
                    |err| {
                        debug!(
                            "auto-lock candidate check failed for {}@{} in {}: {}",
                            tv.short(),
                            tv.version,
                            display_path(&lockfile_path),
                            err
                        );
                        false
                    },
                )
            })
            .collect();
        if versions.is_empty() {
            continue;
        }

        let semaphore = Arc::new(Semaphore::new(jobs));
        let mut jset: JoinSet<LockResolutionResult> = JoinSet::new();

        for tv in &versions {
            let ba = tv.ba().clone();
            let backend = crate::backend::get(&ba);

            for platform in &target_platforms {
                // Expand platform variants from the backend
                let variants = if let Some(ref backend) = backend {
                    backend.platform_variants(platform)
                } else {
                    vec![platform.clone()]
                };

                for variant in variants {
                    let platform_key = variant.to_key();
                    let target = PlatformTarget::new(variant.clone());

                    // Skip if this tool/version/platform already has both checksum and URL
                    if let Some(ref backend) = backend
                        && let Ok(options) = backend.resolve_lockfile_options(&tv.request, &target)
                        && let Some(tools) = lockfile.tools.get(&ba.short)
                        && let Some(tool) = tools
                            .iter()
                            .find(|t| t.version == tv.version && t.options == options)
                        && let Some(info) = tool.platforms.get(&platform_key)
                        && info.checksum.is_some()
                        && info.url.is_some()
                    {
                        continue;
                    }

                    let semaphore = semaphore.clone();
                    let ba = ba.clone();
                    let tv = (*tv).clone();
                    let backend = backend.clone();

                    jset.spawn(async move {
                        let _permit = semaphore.acquire().await;
                        resolve_tool_lock_info(ba, tv, variant, backend).await
                    });
                }
            }
        }

        // Collect results and update lockfile
        // Defer provenance errors until after saving so unaffected tools' entries aren't lost.
        let mut provenance_errors: Vec<String> = Vec::new();
        while let Some(result) = jset.join_next().await {
            match result {
                Ok(resolution) => {
                    if let Err(msg) = &resolution.4 {
                        debug!("auto-lock: {msg}");
                    }
                    if let Err(e) = apply_lock_result(&mut lockfile, resolution) {
                        provenance_errors.push(e.to_string());
                    }
                }
                Err(e) => {
                    debug!("auto-lock task failed: {}", e);
                }
            }
        }

        lockfile.save(&lockfile_path)?;

        all_provenance_errors.extend(provenance_errors);
    }

    if !all_provenance_errors.is_empty() {
        return Err(eyre!("{}", all_provenance_errors.join("\n")));
    }

    Ok(())
}

/// Result type for lock resolution tasks (shared by `mise lock` and auto-lock).
///
/// Fields: (short_name, version, backend_full, platform, info_or_error, options, conda_packages).
/// The `info_or_error` field is `Ok(info)` on success or `Err(message)` on failure,
/// allowing callers to log at the appropriate level.
pub type LockResolutionResult = (
    String,
    String,
    String,
    Platform,
    Result<PlatformInfo, String>,
    BTreeMap<String, String>,
    BTreeMap<String, CondaPackageInfo>,
    BTreeMap<String, PkgxPackageInfo>,
);

/// Resolve lock info for a single tool/platform combination.
///
/// Returns a tuple of (short_name, version, backend_full, platform, info_or_error, options, conda_packages).
/// Does not log errors — callers decide the appropriate log level.
pub async fn resolve_tool_lock_info(
    ba: crate::cli::args::BackendArg,
    tv: ToolVersion,
    platform: Platform,
    backend: Option<crate::backend::ABackend>,
) -> LockResolutionResult {
    let target = PlatformTarget::new(platform.clone());

    let (info, options, conda_packages, pkgx_packages) = if let Some(backend) = backend {
        let options = match backend.resolve_lockfile_options(&tv.request, &target) {
            Ok(options) => options,
            Err(e) => {
                return (
                    ba.short.clone(),
                    tv.version.clone(),
                    ba.stored_full(),
                    platform,
                    Err(e.to_string()),
                    BTreeMap::new(),
                    BTreeMap::new(),
                    BTreeMap::new(),
                );
            }
        };
        match backend.resolve_lock_info(&tv, &target).await {
            Ok(info) => {
                let conda_packages = if backend.get_type() == BackendType::Conda {
                    let conda_backend = CondaBackend::from_arg(ba.clone());
                    match conda_backend.resolve_conda_packages(&tv, &target).await {
                        Ok(packages) => packages,
                        Err(e) => {
                            debug!(
                                "failed to resolve conda packages for {} on {}: {}",
                                ba.short,
                                platform.to_key(),
                                e
                            );
                            BTreeMap::new()
                        }
                    }
                } else {
                    BTreeMap::new()
                };
                let pkgx_packages = if backend.get_type() == BackendType::Pkgx {
                    let pkgx_backend = PkgxBackend::from_arg(ba.clone());
                    match pkgx_backend.resolve_pkgx_packages(&tv, &target).await {
                        Ok(packages) => packages,
                        Err(e) => {
                            return (
                                ba.short.clone(),
                                tv.version.clone(),
                                ba.stored_full(),
                                platform,
                                Err(format!(
                                    "failed to resolve pkgx packages for {} on {}: {}",
                                    ba.short,
                                    target.to_key(),
                                    e
                                )),
                                options,
                                BTreeMap::new(),
                                BTreeMap::new(),
                            );
                        }
                    }
                } else {
                    BTreeMap::new()
                };
                (Ok(info), options, conda_packages, pkgx_packages)
            }
            Err(e) => (
                Err(format!(
                    "failed to resolve {} for {}: {}",
                    ba.short,
                    platform.to_key(),
                    e
                )),
                options,
                BTreeMap::new(),
                BTreeMap::new(),
            ),
        }
    } else {
        (
            Err(format!("backend not found for {}", ba.short)),
            BTreeMap::new(),
            BTreeMap::new(),
            BTreeMap::new(),
        )
    };

    (
        ba.short.clone(),
        tv.version.clone(),
        ba.full(),
        platform,
        info,
        options,
        conda_packages,
        pkgx_packages,
    )
}

/// Apply a lock resolution result to a lockfile, updating platform info and conda packages.
/// Only applies data when the resolution succeeded (info is `Ok`).
///
/// Returns an error if a github backend tool loses provenance on version upgrade,
/// which could indicate a supply chain attack.
pub fn apply_lock_result(lockfile: &mut Lockfile, result: LockResolutionResult) -> Result<()> {
    let (short, version, backend, platform, info, options, conda_packages, pkgx_packages) = result;
    let platform_key = platform.to_key();
    if let Ok(ref info) = info {
        if let Some(err) = check_single_tool_provenance(
            lockfile.tools.get(&short),
            &short,
            &version,
            &backend,
            &platform_key,
            info.provenance.as_ref(),
        ) {
            return Err(eyre!("{err}"));
        }
        lockfile.set_platform_info(
            &short,
            &version,
            Some(&backend),
            &options,
            &platform_key,
            info.clone(),
        );
    }
    for (basename, pkg_info) in conda_packages {
        lockfile.set_conda_package(&platform_key, &basename, pkg_info);
    }
    for (id, pkg_info) in pkgx_packages {
        lockfile.set_pkgx_package(&platform_key, &id, pkg_info);
    }
    Ok(())
}

type RekeyDecisions = HashMap<(String, String), Option<BTreeMap<String, String>>>;

fn record_rekey_decision(
    decisions: &mut RekeyDecisions,
    key: (String, String),
    resolved: Option<BTreeMap<String, String>>,
) {
    use std::collections::hash_map::Entry;

    match decisions.entry(key) {
        Entry::Vacant(entry) => {
            entry.insert(resolved);
        }
        Entry::Occupied(mut entry) if entry.get() != &resolved => {
            // Resolution errors and disagreement between same-version requests
            // both fail closed: leave the legacy platform where it is.
            entry.insert(None);
        }
        Entry::Occupied(_) => {}
    }
}

fn build_rekey_decisions(
    versions: &[ToolVersion],
    existing_tools: Option<&Vec<LockfileTool>>,
) -> RekeyDecisions {
    let legacy_platforms: HashMap<&str, BTreeSet<&str>> = existing_tools
        .into_iter()
        .flatten()
        .filter(|tool| tool.options.is_empty())
        .fold(HashMap::new(), |mut platforms, tool| {
            platforms
                .entry(tool.version.as_str())
                .or_default()
                .extend(tool.platforms.keys().map(String::as_str));
            platforms
        });
    let mut decisions = HashMap::new();

    for tv in versions {
        let Some(platforms) = legacy_platforms.get(tv.version.as_str()) else {
            continue;
        };
        for platform_key in platforms {
            let resolved = Platform::parse(platform_key).ok().and_then(|platform| {
                let backend = tv.request.backend().ok()?;
                backend
                    .resolve_lockfile_options(&tv.request, &PlatformTarget::new(platform))
                    .ok()
            });
            record_rekey_decision(
                &mut decisions,
                (tv.version.clone(), (*platform_key).to_string()),
                resolved,
            );
        }
    }

    decisions
}

/// Merge tool entries with deduplication by (version, options).
/// Merges platform info for entries with the same key.
/// Preserves existing platform info for matching entries.
/// Existing empty-options entries are rekeyed platform-by-platform using the
/// backend's resolved options. This migrates legacy entries without guessing
/// from platform overlap while preserving data when resolution is ambiguous.
fn merge_tool_entries<F>(
    entries: Vec<LockfileTool>,
    existing_tools: Option<&Vec<LockfileTool>>,
    mut resolve: F,
) -> MergeToolEntriesResult
where
    F: FnMut(&str, &Platform) -> Option<BTreeMap<String, String>>,
{
    // Group by (version, options) - the key for deduplication
    let mut by_key: HashMap<(String, BTreeMap<String, String>), LockfileTool> = HashMap::new();
    let mut consumed_keys = HashSet::new();

    for tool in entries {
        let key = (tool.version.clone(), tool.options.clone());
        let entry = by_key.entry(key).or_insert_with(|| tool.clone());

        // Merge platforms - properly combine platform info to preserve URLs and prefer sha256
        for (platform, info) in tool.platforms {
            entry
                .platforms
                .entry(platform)
                .and_modify(|existing| *existing = info.merge_with(existing))
                .or_insert(info);
        }
    }
    let fresh_versions: HashSet<String> =
        by_key.keys().map(|(version, _)| version.clone()).collect();

    // Merge with existing tools to preserve platform info
    if let Some(existing) = existing_tools {
        for existing_tool in existing {
            let key = (existing_tool.version.clone(), existing_tool.options.clone());
            if !existing_tool.options.is_empty() {
                if let Some(entry) = by_key.get_mut(&key) {
                    // Preserve platform data only for an exact non-empty option
                    // match. Moving it to another real variant could attach a
                    // stale checksum to a different artifact.
                    for (platform, info) in &existing_tool.platforms {
                        entry
                            .platforms
                            .entry(platform.clone())
                            .and_modify(|existing| *existing = existing.merge_with(info))
                            .or_insert(info.clone());
                    }
                }
                continue;
            }
            if !fresh_versions.contains(&existing_tool.version) {
                continue;
            }

            for (platform_key, info) in &existing_tool.platforms {
                let resolved_options = Platform::parse(platform_key)
                    .ok()
                    .and_then(|platform| resolve(&existing_tool.version, &platform));
                let Some(resolved_options) = resolved_options else {
                    by_key
                        .entry(key.clone())
                        .or_insert_with(|| LockfileTool {
                            version: existing_tool.version.clone(),
                            backend: existing_tool.backend.clone(),
                            options: existing_tool.options.clone(),
                            platforms: BTreeMap::new(),
                        })
                        .platforms
                        .insert(platform_key.clone(), info.clone());
                    continue;
                };

                let target_key = (existing_tool.version.clone(), resolved_options.clone());
                let target = by_key.entry(target_key).or_insert_with(|| LockfileTool {
                    version: existing_tool.version.clone(),
                    backend: existing_tool.backend.clone(),
                    options: resolved_options,
                    platforms: BTreeMap::new(),
                });
                target
                    .platforms
                    .entry(platform_key.clone())
                    .and_modify(|fresh| *fresh = fresh.merge_with(info))
                    .or_insert_with(|| info.clone());
            }
            if !by_key.contains_key(&key) {
                consumed_keys.insert(key);
            }
        }
    }

    let tools = by_key
        .into_values()
        .sorted_by(|a, b| {
            a.version
                .cmp(&b.version)
                .then_with(|| a.options.cmp(&b.options))
        })
        .collect();
    (tools, consumed_keys)
}

fn preserve_absent_tool_entries(
    merged_tools: &mut Vec<LockfileTool>,
    existing_tools: Option<&Vec<LockfileTool>>,
    consumed_keys: &HashSet<LockfileToolKey>,
) {
    let Some(existing_tools) = existing_tools else {
        return;
    };
    let mut keys: HashSet<(String, BTreeMap<String, String>)> = merged_tools
        .iter()
        .map(|tool| (tool.version.clone(), tool.options.clone()))
        .collect();

    for existing_tool in existing_tools {
        let key = (existing_tool.version.clone(), existing_tool.options.clone());
        if keys.contains(&key) || consumed_keys.contains(&key) {
            continue;
        }
        if keys.insert(key) {
            merged_tools.push(existing_tool.clone());
        }
    }
    merged_tools.sort_by(|a, b| {
        a.version
            .cmp(&b.version)
            .then_with(|| a.options.cmp(&b.options))
    });
}

fn read_all_lockfiles(config: &Config) -> Arc<Lockfile> {
    // Create a cache key from the config file paths
    let monorepo_root = config.monorepo_lockfile_root();
    let legacy_lockfiles = monorepo_legacy_lockfile_paths(config);
    let cache_key: Vec<PathBuf> = config
        .config_files
        .keys()
        .map(|path| lockfile_path_for_config(path, monorepo_root.as_deref()).0)
        .chain(legacy_lockfiles.iter().cloned())
        .unique()
        .collect();

    // Use unwrap_or_else to recover from poisoned mutex (thread panicked while holding lock)
    let mut cache = ALL_LOCKFILES_CACHE
        .lock()
        .unwrap_or_else(|e| e.into_inner());
    if let Some(cached) = cache.get(&cache_key) {
        return Arc::clone(cached);
    }

    let mut seen_roots: HashSet<PathBuf> = HashSet::new();
    let mut all: Vec<Lockfile> = Vec::new();

    for (path, cf) in config.config_files.iter().rev() {
        if !cf.source().is_mise_toml() {
            continue;
        }

        let (lockfile_path, _) = lockfile_path_for_config(path, monorepo_root.as_deref());
        let root = lockfile_path.parent().unwrap_or(path).to_path_buf();
        if seen_roots.contains(&root) {
            continue;
        }
        seen_roots.insert(root.clone());

        // Read lockfiles in priority order (highest first):
        // 1. mise.<env>.local.lock (explicit MISE_ENV, then auto platform envs)
        // 2. mise.local.lock
        // 3. mise.<env>.lock (explicit MISE_ENV, then auto platform envs)
        // 4. mise.lock
        // AUTO_ENV_NAMES is ordered least to most specific, so reverse it here
        // to read the most specific (e.g. macos-arm64) first.
        for env_name in env::MISE_ENV.iter().chain(env::AUTO_ENV_NAMES.iter().rev()) {
            let p = root.join(format!("mise.{env_name}.local.lock"));
            if let Ok(l) = Lockfile::read(&p) {
                all.push(l);
            }
        }
        let local_path = root.join("mise.local.lock");
        if let Ok(local) = Lockfile::read(&local_path) {
            all.push(local);
        }
        for env_name in env::MISE_ENV.iter().chain(env::AUTO_ENV_NAMES.iter().rev()) {
            let p = root.join(format!("mise.{env_name}.lock"));
            if let Ok(l) = Lockfile::read(&p) {
                all.push(l);
            }
        }
        let main_path = root.join("mise.lock");
        if let Ok(main) = Lockfile::read(&main_path) {
            all.push(main);
        }
    }
    for legacy_path in legacy_lockfiles {
        if let Ok(legacy) = Lockfile::read(&legacy_path) {
            all.push(legacy);
        }
    }

    let result = all.into_iter().fold(Lockfile::default(), |mut acc, l| {
        for (short, tools) in l.tools {
            let existing = acc.tools.entry(short).or_default();
            for tool in tools {
                // Avoid duplicates (same version+options)
                if !existing
                    .iter()
                    .any(|t| t.version == tool.version && t.options == tool.options)
                {
                    existing.push(tool);
                }
            }
        }
        acc
    });

    let result = Arc::new(result);
    cache.insert(cache_key, Arc::clone(&result));
    result
}

fn read_lockfile_for(config: &Config, path: &Path) -> Arc<Lockfile> {
    let monorepo_root = config.monorepo_lockfile_root();
    let (lockfile_path, _is_local) = lockfile_path_for_config(path, monorepo_root.as_deref());
    let legacy_path = monorepo_root
        .as_deref()
        .and_then(|root| legacy_lockfile_path_for_config(config, path, root));

    read_lockfile_at(lockfile_path, legacy_path)
}

fn read_lockfile_at(lockfile_path: PathBuf, legacy_path: Option<PathBuf>) -> Arc<Lockfile> {
    let cache_key: Vec<PathBuf> = std::iter::once(lockfile_path.clone())
        .chain(legacy_path.iter().cloned())
        .collect();

    // Use unwrap_or_else to recover from poisoned mutex (thread panicked while holding lock)
    let mut cache = SINGLE_LOCKFILE_CACHE
        .lock()
        .unwrap_or_else(|e| e.into_inner());
    if let Some(cached) = cache.get(&cache_key) {
        return Arc::clone(cached);
    }

    let mut lockfile = Lockfile::read(&lockfile_path)
        .unwrap_or_else(|err| handle_lockfile_read_error(err, &lockfile_path));
    if let Some(legacy_path) = &legacy_path
        && let Err(err) = merge_legacy_lockfile_if_present(&mut lockfile, legacy_path)
    {
        warn!(
            "failed to read legacy monorepo lockfile {} (possible corruption): {err:?}",
            display_path(legacy_path)
        );
    }

    let lockfile = Arc::new(lockfile);
    cache.insert(cache_key, Arc::clone(&lockfile));
    lockfile
}

pub(crate) fn read_lockfile_for_config_path(config: &Config, path: &Path) -> Lockfile {
    read_lockfile_for(config, path).as_ref().clone()
}

pub(crate) fn read_lockfile_for_tool_source(
    config: &Config,
    source: &ToolSource,
) -> Result<Lockfile> {
    if let ToolSource::MiseToml(path) = source {
        return Ok(read_lockfile_for_config_path(config, path));
    }

    let monorepo_root = config.monorepo_lockfile_root();
    let Some((lockfile_path, _)) =
        lockfile_path_for_tool_source_with_root(config, source, monorepo_root.as_deref())
    else {
        return Ok(Lockfile::default());
    };
    let legacy_path = monorepo_root
        .as_deref()
        .and_then(|_| lockfile_path_for_tool_source_with_root(config, source, None))
        .map(|(path, _)| path)
        .filter(|path| path != &lockfile_path);

    Ok(read_lockfile_at(lockfile_path, legacy_path)
        .as_ref()
        .clone())
}

pub fn get_locked_version(
    config: &Config,
    path: Option<&Path>,
    short: &str,
    prefix: &str,
    request_options: &BTreeMap<String, String>,
) -> Result<Option<LockfileTool>> {
    let settings = Settings::get();
    if !settings.lockfile_enabled() {
        return Ok(None);
    }

    let lockfile = match path {
        Some(path) => {
            trace!(
                "[{short}@{prefix}] reading lockfile for {}",
                display_path(path)
            );
            read_lockfile_for(config, path)
        }
        None => {
            trace!("[{short}@{prefix}] reading all lockfiles");
            read_all_lockfiles(config)
        }
    };

    if let Some(tools) = lockfile.tools.get(short) {
        // Filter by version prefix and options
        let mut matching: Vec<_> = tools
            .iter()
            .filter(|v| {
                lockfile_version_matches(prefix, &v.version) && &v.options == request_options
            })
            .collect();

        // Only sort when prefix is "latest" and we have multiple matches
        // This is expensive, so avoid it for specific version prefixes
        if prefix == "latest" && matching.len() > 1 {
            matching.sort_by(|a, b| {
                versions::Versioning::new(&b.version).cmp(&versions::Versioning::new(&a.version))
            });
        }

        if let Some(found) = matching.first() {
            trace!("[{short}@{prefix}] found {} in lockfile", found.version);
            return Ok(Some((*found).clone()));
        }
    }

    Ok(None)
}

fn lockfile_version_matches(prefix: &str, version: &str) -> bool {
    prefix == "latest" || strip_leading_v(version).starts_with(strip_leading_v(prefix))
}

fn strip_leading_v(version: &str) -> &str {
    version
        .strip_prefix(['v', 'V'])
        .filter(|stripped| stripped.chars().next().is_some_and(|c| c.is_ascii_digit()))
        .unwrap_or(version)
}

/// Get the backend for a tool from the lockfile, ignoring options.
/// This is used for backend discovery where we just need any entry's backend.
pub fn get_locked_backend(config: &Config, short: &str) -> Option<String> {
    let settings = Settings::get();
    if !settings.lockfile_enabled() {
        return None;
    }

    let lockfile = read_all_lockfiles(config);

    lockfile
        .tools
        .get(short)
        .and_then(|tools| tools.first())
        .and_then(|tool| tool.backend.clone())
}

fn handle_lockfile_read_error(err: Report, lockfile_path: &Path) -> Lockfile {
    // Differentiate between "file not found" (fine) and "parse error" (problem)
    // File not found is expected when lockfile doesn't exist yet
    if let Some(io_err) = err.downcast_ref::<std::io::Error>()
        && io_err.kind() == std::io::ErrorKind::NotFound
    {
        trace!(
            "lockfile {} not found, using empty lockfile",
            display_path(lockfile_path)
        );
        return Lockfile::default();
    }
    // For other errors (parse errors, permission issues), warn the user
    // as this could indicate data loss or corruption
    warn!(
        "failed to read lockfile {} (possible corruption): {err:?}",
        display_path(lockfile_path)
    );
    Lockfile::default()
}

#[cfg(unix)]
fn apply_lockfile_permissions(tmp: &tempfile::NamedTempFile, target: &Path) -> Result<()> {
    use std::os::unix::fs::PermissionsExt;

    match fs::metadata(target) {
        Ok(metadata) => {
            let mode = metadata.permissions().mode();
            tmp.as_file()
                .set_permissions(fs::Permissions::from_mode(mode))?;
        }
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => {}
        Err(err) => return Err(err.into()),
    }
    Ok(())
}

#[cfg(not(unix))]
fn apply_lockfile_permissions(_tmp: &tempfile::NamedTempFile, _target: &Path) -> Result<()> {
    Ok(())
}

fn persist_lockfile_tmp(mut tmp: tempfile::NamedTempFile, target: &Path) -> Result<()> {
    const RETRIES: u32 = 20;

    for attempt in 0..=RETRIES {
        match tmp.persist(target) {
            Ok(_) => return Ok(()),
            Err(err) if should_retry_lockfile_persist(&err.error) && attempt < RETRIES => {
                tmp = err.file;
                std::thread::sleep(std::time::Duration::from_millis(5 * u64::from(attempt + 1)));
            }
            Err(err) => return Err(err.error.into()),
        }
    }

    unreachable!("lockfile persist retry loop should always return");
}

#[cfg(windows)]
fn should_retry_lockfile_persist(err: &std::io::Error) -> bool {
    err.kind() == std::io::ErrorKind::PermissionDenied
}

#[cfg(not(windows))]
fn should_retry_lockfile_persist(_err: &std::io::Error) -> bool {
    false
}

impl TryFrom<toml::Value> for LockfileTool {
    type Error = Report;
    fn try_from(value: toml::Value) -> Result<Self> {
        let tool = match value {
            toml::Value::String(v) => LockfileTool {
                version: v,
                backend: Default::default(),
                options: Default::default(),
                platforms: Default::default(),
            },
            toml::Value::Table(mut t) => {
                let mut platforms = BTreeMap::new();
                // Handle nested platforms table format: [tools.X.platforms.linux-x64]
                if let Some(platforms_table) = t.remove("platforms") {
                    let platforms_table: toml::Table = platforms_table.try_into()?;
                    for (platform, platform_info) in platforms_table {
                        platforms.insert(platform, platform_info.try_into()?);
                    }
                }
                // Handle inline table format: "platforms.linux-x64" = { ... }
                let platform_keys: Vec<_> = t
                    .keys()
                    .filter(|k| k.starts_with("platforms."))
                    .cloned()
                    .collect();
                for key in platform_keys {
                    if let Some(platform_info) = t.remove(&key) {
                        let platform_name = key.strip_prefix("platforms.").unwrap().to_string();
                        platforms.insert(platform_name, platform_info.try_into()?);
                    }
                }
                let mut options = BTreeMap::new();
                if let Some(opts) = t.remove("options") {
                    let opts_table: toml::Table = opts.try_into()?;
                    for (key, value) in opts_table {
                        if let toml::Value::String(s) = value {
                            options.insert(key, s);
                        }
                    }
                }
                // Silently discard env field from old lockfiles for backwards compat
                t.remove("env");
                LockfileTool {
                    version: t
                        .remove("version")
                        .map(|v| v.try_into())
                        .transpose()?
                        .unwrap_or_default(),
                    backend: t
                        .remove("backend")
                        .map(|v| v.try_into())
                        .transpose()?
                        .unwrap_or_default(),
                    options,
                    platforms,
                }
            }
            _ => bail!("unsupported lockfile format {}", value),
        };
        Ok(tool)
    }
}

impl LockfileTool {
    fn into_toml_value(self) -> toml::Value {
        let mut table = toml::Table::new();
        table.insert("version".to_string(), self.version.into());
        if let Some(backend) = self.backend {
            table.insert("backend".to_string(), backend.into());
        }
        if !self.options.is_empty() {
            let opts_table: toml::Table = self
                .options
                .into_iter()
                .map(|(k, v)| (k, toml::Value::String(v)))
                .collect();
            table.insert("options".to_string(), toml::Value::Table(opts_table));
        }
        if !self.platforms.is_empty() {
            table.insert("platforms".to_string(), self.platforms.clone().into());
        }
        table.into()
    }
}

fn lockfile_tool_from_tool_version(tv: &ToolVersion) -> Result<LockfileTool> {
    let mut platforms = BTreeMap::new();

    // Convert tool version lock_platforms to lockfile platforms
    for (platform, platform_info) in &tv.lock_platforms {
        platforms.insert(platform.clone(), platform_info.clone());
    }

    // Resolve lockfile options from the backend
    let options = if let Ok(backend) = tv.request.backend() {
        let target = PlatformTarget::from_current();
        backend.resolve_lockfile_options(&tv.request, &target)?
    } else {
        BTreeMap::new()
    };

    Ok(LockfileTool {
        version: tv.version.clone(),
        backend: Some(tv.ba().stored_full()),
        options,
        platforms,
    })
}

fn format(mut doc: DocumentMut) -> String {
    if let Some(tools) = doc.get_mut("tools") {
        for (_k, v) in tools.as_table_mut().unwrap().iter_mut() {
            if let toml_edit::Item::ArrayOfTables(art) = v {
                for t in art.iter_mut() {
                    t.sort_values_by(|a, _, b, _| {
                        if a == "version" {
                            return std::cmp::Ordering::Less;
                        }
                        if b == "version" {
                            return std::cmp::Ordering::Greater;
                        }
                        a.to_string().cmp(&b.to_string())
                    });
                    // TODO: use TOML 1.1 multiline inline tables once toml_edit supports
                    // InlineTable::set_multiline(). See https://github.com/toml-rs/toml/issues/1027
                    // Convert platforms to dotted-key subtables (multi-line)
                    if let Some(toml_edit::Item::Table(platforms_table)) = t.remove("platforms") {
                        for (platform_key, platform_value) in platforms_table.iter() {
                            if let toml_edit::Item::Table(platform_info) = platform_value {
                                let dotted_key = format!("platforms.{}", platform_key);
                                let mut subtable = toml_edit::Table::new();
                                let mut keys: Vec<_> =
                                    platform_info.iter().map(|(k, _)| k.to_string()).collect();
                                keys.sort_by_key(|k| match k.as_str() {
                                    "checksum" => 0,
                                    "size" => 1,
                                    "url" => 2,
                                    "url_api" => 3,
                                    "provenance" => 4,
                                    _ => 5,
                                });
                                for k in &keys {
                                    if let Some(item) = platform_info.get(k) {
                                        subtable.insert(k, item.clone());
                                    }
                                }
                                subtable.set_implicit(true);
                                t.insert(&dotted_key, toml_edit::Item::Table(subtable));
                            }
                        }
                    }
                }
            }
        }
    }

    doc.to_string()
}

